    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        if (current_index >= 0 && current_index < records.size() - 1 && records.size() > 0 && records != null) {
            current_index++;
            displayRecord();
        } else {
            JOptionPane.showMessageDialog(this, "There is No Record");
        }
    }//GEN-LAST:event_btnNextActionPerformed

    private void btnPreviousActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPreviousActionPerformed
        if (current_index > 0 && current_index < records.size() && records.size() > 0 && records != null) {
            current_index--;
            displayRecord();
        } else {
            JOptionPane.showMessageDialog(this, "There is No Record");
        }
    }//GEN-LAST:event_btnPreviousActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String message = "";
        MemberDAO dao = new MemberDAO();
        String memId = jlMemID.getText();
        int option = JOptionPane.showConfirmDialog(this, "Are you sure you to delete this record");
        if (option == JOptionPane.YES_OPTION) {
            if (!memId.isEmpty()) {
                if (dao.deleteRecord(memId)) {
                    message += "Selected Record is deleted";
                    getRecord();
                    displayRecord();
                } else {
                    message += "deletion error due to : " + dao.getEror();
                }
            } else {
                message += "no field selected";
            }
            JOptionPane.showMessageDialog(this, message);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


