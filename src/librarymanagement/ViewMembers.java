    private void getRecord() {
        records = new MemberDAO().getAllRecord();
        current_index = 0;
        if (records.size() > 0 && records != null) {
            btnDelete.setEnabled(true);
            btnUpdate.setEnabled(true);
        } else {
            btnDelete.setEnabled(false);
            btnDelete.setEnabled(false);
        }
    }

    private void displayRecord() {
        if (current_index >= 0 && current_index < records.size() && records.size() >= 0 && records != null) {
            MemberTO rec = records.get(current_index);
            jlMemID.setText(rec.getMemberId());
            jlMem_name.setText(rec.getMemberName());
            jlMemAdd.setText(rec.getAddress());
            jlJoinDate.setText(rec.getJoindate().toString());
            jlDOB.setText(rec.getDob().toString());
            jlIssued.setText(String.valueOf(rec.getIssuedBooks()));
            jlReturned.setText(String.valueOf(rec.getReturnBooks()));
        }
    }

