    public void bindBookType() {
        jcbBookTypes.removeAllItems();
        jcbBookTypes.addItem("--BOOK TYPES--");
        BookTypeDAO dao = new BookTypeDAO();
        records = dao.getAllRecord();
        if (records != null && records.size() > 0) {
            
            for (BookTypeTO rec : records) {
                jcbBookTypes.addItem(rec.getBookType());
            }
        }
    }


