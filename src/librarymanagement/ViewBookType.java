    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int type = jcbBookTypes.getSelectedIndex();
        String message = "";
        if (type > 0) {
            int option = JOptionPane.showConfirmDialog(this, "Are You Sure To Remove This Record?", "Important Message", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                BookTypeTO rec = new BookTypeTO();
                rec.setBookType(jcbBookTypes.getItemAt(type));
                BookTypeDAO dao = new BookTypeDAO();
                if (dao.deleteRecord(rec.getBookType())) {
                    message += "Given record is deleted";
                } else {
                    message += "Deletion error due to:" + dao.getEror();
                }
            }
        } else {
            message += "Select A record";
        }
        JOptionPane.showMessageDialog(this, message);
    }//GEN-LAST:event_btnDeleteActionPerformed

